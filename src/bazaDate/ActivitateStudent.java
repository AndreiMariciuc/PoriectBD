package bazaDate;
public class ActivitateStudent {
    private int id_activitate;
    private String denumireActivitate;
    private int numarMaxParticipanti;

    public int getId_activitate() {
        return id_activitate;
    }

    public void setId_activitate(int id_activitate) {
        this.id_activitate = id_activitate;
    }

    public String getDenumireActivitate() {
        return denumireActivitate;
    }

    public void setDenumireActivitate(String denumireActivitate) {
        this.denumireActivitate = denumireActivitate;
    }

    public int getNumarMaxParticipanti() {
        return numarMaxParticipanti;
    }

    public void setNumarMaxParticipanti(int numarMaxParticipanti) {
        this.numarMaxParticipanti = numarMaxParticipanti;
    }
}
